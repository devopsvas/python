#!/usr/bin/python3

mystring = "testpython"
print(mystring[1])

# To print form index to all the way to the end

print(mystring[2:])

# To print all the values up to letter "P", Stop notation is up to not including

print(mystring[:4])


# To grab the "stp" form the middle of the string 


print(mystring[2:5])

# To grab everything form the beginning to the end

print(mystring[::])

# To display the string with step size of 2 

print(mystring[::2])

# To Display the string form staring 2  to 7 with steps size of 2

print(mystring[2:7:2])

# Reverse the string 

print(mystring[::-1])




